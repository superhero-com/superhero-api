  status profile_x_invite_milestone_rewards_status_enum NOT NULL DEFAULT 'pending',
  error text NULL,
  created_at timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
  updated_at timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP(6)
);

CREATE UNIQUE INDEX IF NOT EXISTS profile_x_invite_milestone_rewards_inviter_threshold_uq
