  ON profile_x_invite_challenges (invite_code);

CREATE TABLE IF NOT EXISTS profile_x_invites (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  inviter_address character varying NOT NULL,
  invitee_address character varying NULL,
  code character varying NOT NULL,
  status profile_x_invites_status_enum NOT NULL DEFAULT 'active',
  bound_at timestamp NULL,
  created_at timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP(6)
);

